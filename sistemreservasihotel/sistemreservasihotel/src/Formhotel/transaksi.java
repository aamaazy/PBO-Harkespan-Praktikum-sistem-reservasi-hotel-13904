    public void insert(String kode, int nokmar, String jeniskmar, String checkin, String checkout,int lama, int total) {
    try {
        Class.forName(JDBC_DRIVER);
        conn = DriverManager.getConnection(DB_URL, USER, PASS);
        stmt = conn.createStatement();

        // Mengecek apakah kode barang sudah ada di database
        String checkQuery = "SELECT * FROM transaksi WHERE kd_kmr = ?";
        PreparedStatement checkStatement = conn.prepareStatement(checkQuery);
        checkStatement.setString(1, kode);
        ResultSet checkResult = checkStatement.executeQuery();

        if (checkResult.next()) {
            // Kode barang sudah ada, tampilkan pesan dialog
            JOptionPane.showMessageDialog(this, "Kode Kamar sudah ada!");
        } else {
            // Kode barang belum ada, lakukan operasi INSERT
            String insertQuery = "INSERT INTO transaksi (kd_kmr, no_kmr, jenis_kmr,check_in,check_oout,lama,total) VALUES (?,?,?,?,?,?,?)";
            PreparedStatement insertStatement = conn.prepareStatement(insertQuery);
            insertStatement.setString(1, kode);
            insertStatement.setInt(2, nokmar);
            insertStatement.setString(3, jeniskmar);
            insertStatement.setString(4, checkin);
            insertStatement.setString(5, checkout);
            insertStatement.setInt(6, lama);
            insertStatement.setInt(7, total);

            insertStatement.execute();
        }

        stmt.close();
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}
    
    public void showtable(){
        try{
            Class.forName(JDBC_DRIVER);	   
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Kode Kamar");
            model.addColumn("No Kamar");
            model.addColumn("Jenis Kamar");
            model.addColumn("Check IN");
            model.addColumn("Check OUT");
            model.addColumn("Lama Menginap");
            model.addColumn("Total");
        
 
            stmt = conn.createStatement();
            String sql = "SELECT * FROM transaksi";
            //int i = 1;
            rs = stmt.executeQuery(sql);
            while(rs.next()) {
            	String kode= rs.getString("kd_kmr");
            	int nomor = rs.getInt("no_kmr");
            	String jenis = rs.getString("jenis_kmr");
                String checkIn=rs.getString("check_in");
                String checkOut=rs.getString("check_oout");
                int lamaNginap=rs.getInt("lama");
            	int total=rs.getInt("total");
                model.addRow (new Object[]{kode,nomor,jenis,checkIn,checkOut,lamaNginap,total});
            }
            rs.close();
            conn.close();
            stmt.close();
            
            tabelData.setModel(model);
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
    

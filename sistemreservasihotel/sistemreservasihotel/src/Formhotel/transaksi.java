            String sql = "UPDATE transaksi SET kd_kmr=?, no_kmr=?, jenis_kmr=?,check_in=?,check_oout=?,lama=?, total=? WHERE kd_kmr='"+kode+"'";
            
            PreparedStatement pms = conn.prepareStatement(sql);
            pms.setString(1, kode);
